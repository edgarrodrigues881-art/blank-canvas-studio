interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
}

export function EmojiPicker({ onEmojiSelect }: EmojiPickerProps) {
  const [open, setOpen] = useState(false);
  const [activeCategory, setActiveCategory] = useState(0);
  const [search, setSearch] = useState("");
  const containerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!open) return;
    const handler = (e: MouseEvent) => {
      if (containerRef.current && !containerRef.current.contains(e.target as Node)) {
        setOpen(false);
      }
    };
    document.addEventListener("mousedown", handler);
    return () => document.removeEventListener("mousedown", handler);
  }, [open]);

  const filteredEmojis = search.trim()
    ? EMOJI_CATEGORIES.flatMap((c) => c.emojis)
    : EMOJI_CATEGORIES[activeCategory]?.emojis || [];

  const handleSelect = useCallback((emoji: string) => {
    onEmojiSelect(emoji);
  }, [onEmojiSelect]);

  return (
    <div ref={containerRef} className="relative">
      <Button
        variant="ghost"
        size="icon"
        className="w-9 h-9 shrink-0 text-muted-foreground hover:text-foreground mb-0.5"
        onClick={() => { setOpen((v) => !v); setSearch(""); }}
        type="button"
      >
        <Smile className="w-4 h-4" />
      </Button>

      {open && (
        <div className="absolute bottom-12 left-0 z-50 w-[320px] bg-popover border border-border rounded-xl shadow-xl overflow-hidden animate-in fade-in slide-in-from-bottom-2 duration-150">
          {/* Category tabs */}
          <div className="flex items-center gap-0.5 px-2 py-1.5 border-b border-border/50 bg-muted/30">
            {EMOJI_CATEGORIES.map((cat, i) => (
              <button
                key={cat.name}
                onClick={() => { setActiveCategory(i); setSearch(""); }}
                className={`flex-1 text-center py-1 rounded-md text-base transition-colors ${
                  activeCategory === i && !search ? "bg-primary/15" : "hover:bg-muted/50"
                }`}
                title={cat.name}
              >
                {cat.icon}
              </button>
            ))}
          </div>

          {/* Search */}
          <div className="px-2 py-1.5 border-b border-border/30">
            <div className="relative">
              <Search className="absolute left-2 top-1/2 -translate-y-1/2 w-3.5 h-3.5 text-muted-foreground/50" />
              <input
                type="text"
                placeholder="Pesquisar emoji..."
                value={search}
                onChange={(e) => setSearch(e.target.value)}
                className="w-full bg-muted/30 border border-border/50 rounded-lg pl-7 pr-2 py-1.5 text-xs text-foreground placeholder:text-muted-foreground/50 focus:outline-none focus:ring-1 focus:ring-primary/30"
              />
              {search && (
                <button onClick={() => setSearch("")} className="absolute right-2 top-1/2 -translate-y-1/2">
                  <X className="w-3 h-3 text-muted-foreground" />
                </button>
              )}
            </div>
          </div>

          {/* Category label */}
          {!search && (
            <div className="px-3 pt-1.5 pb-0.5">
              <span className="text-[10px] font-semibold text-muted-foreground uppercase tracking-wider">
                {EMOJI_CATEGORIES[activeCategory]?.name}
              </span>
            </div>
          )}

          {/* Emoji grid */}
          <div className="h-[220px] overflow-y-auto px-2 py-1 scrollbar-thin">
            <div className="grid grid-cols-8 gap-0.5">
              {filteredEmojis.map((emoji, i) => (
                <button
                  key={`${emoji}-${i}`}
                  onClick={() => handleSelect(emoji)}
                  className="w-9 h-9 flex items-center justify-center rounded-md hover:bg-muted/50 active:scale-90 transition-all text-xl"
                >
                  {emoji}
                </button>
              ))}
            </div>
            {search && filteredEmojis.length === 0 && (
              <p className="text-center text-xs text-muted-foreground py-8">Nenhum emoji encontrado</p>
            )}
          </div>
        </div>
      )}
    </div>
  );
}
